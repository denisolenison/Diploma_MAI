		function gt(a, b) {
			if (isNaN(a.num)) a = big(a);
			if (isNaN(b.num)) b = big(b);
			if (a.sign == 1 && b.sign == 1) {
				if (a.shift > b.shift) {
					return true;
				}
				else if (a.shift == b.shift) {
					if (a.power > b.power) {
						return true;
					}
					else if (a.power == b.power) {
						if (a.num > b.num) {
							return true;
						}
						else return false;
					}
					else return false;
				}
				else return false;
			}
			else if (a.sign == 1 && b.sign == 0) {
				return true;
			}
			else if (a.sign == 0 && b.sign == 1) {
				return false;
			}
			else {
				if (a.shift > b.shift) {
					return false;
				}
				else if (a.shift == b.shift) {
					if (a.power > b.power) {
						return false;
					}
					else if (a.power == b.power) {
						if (a.num >= b.num) {
							return false;
						}
						else return true;
					}
					else return true;
				}
				else return true;
			}
		}
		
