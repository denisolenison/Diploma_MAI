		function fromStr(str) {
			var sign = 1;
			var shift = 0;
			var num = 0;
			var power = 0;
			
			var wasNum = false;
			var wasPow = false;
			var wasPoint = false;
			var pointS = 1;
			
			if (str[0] == '-') {
				sign = 0;
			}
			for (var i = 0 ; i < str.length ; ++i) {
				if (str[i] == 'e' && !wasNum) {
					shift += 1;
				}
				if (str[i] >= '0' && str[i] <= '9' && !wasPow && !wasPoint) {
					wasNum = true;
					num *= 10;
					num += (str[i] - '0');
				}
				if (str[i] == 'e' && wasNum) {
					wasPow = true
				}
				if (str[i] == '.' && !wasPow) {
					wasPoint = true;
				}
				if (str[i] >= '0' && str[i] <= '9' && !wasPow && wasPoint) {
					num += (str[i] - '0') * Math.pow(0.1, pointS);
					pointS += 1;
				}
				if (str[i] >= '0' && str[i] <= '9' && wasPow) {
					power *= 10;
					power += (str[i] - '0');
				}
			}
			return new BigNum(num, power, shift, sign);
		}
		
		
		
		
